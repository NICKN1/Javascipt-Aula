/*
 Aritméticos
  + Adição / concatenação
  - / * 
  ** Potenciação
  % Resto da divisão
 
  Precedências (Do primeiro ao ultimo)
  ()
  **
  * / %
  +-
 */
const num1 = 2;
const num2 = 10;
const num3 = 5;
console.log(num1 ** num2); // **Ponteciação

console.log(num1 % num2); // %Resto da divisão

console.log((num1 + num2) * num3); // Para mudar a precedência