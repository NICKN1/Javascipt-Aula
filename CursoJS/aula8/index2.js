/* Incremento = ++ (pode ser pré ou pós)
   Decremento = -- (pode ser pré ou pós)

   Operadores de atribuição e potencialização: += -= *= **=
   *NaN = Not a Number
*/

let contador = 1; 
contador++; // o valor da variável mais um
contador++; 
contador++;
contador++;
console.log(++contador); /* agora ele adcionará mais um número
 antes de aparecer na tela */


 contador = 0;
